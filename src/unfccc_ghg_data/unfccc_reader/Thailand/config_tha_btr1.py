def path_to_values(
    path: str,
    zero: str,
) -> list:
    """Extract values from a svg lineplot path

    Will likely not work in a general case without modification
    """
    parts = path.split(" ")
    parts = [part for part in parts if len(part) > 1]
    for i, part in enumerate(parts):
        x_y = part.split(",")
        if len(x_y) == 1:
            if i > 0:
                parts[i] = 0
            else:
                raise ValueError(  # noqa: TRY003
                    f"single number found in first element of path: {path}"
                )
        elif len(x_y) == 2:  # noqa: PLR2004
            parts[i] = float(x_y[1])
        else:
            raise ValueError(  # noqa: TRY003
                f"more than two numbers found in element {i} of path: {path}"
            )
    zero = zero.split(",")[1]
    parts[0] = parts[0] - float(zero)
    for i, part in enumerate(parts[1:]):
        parts[i + 1] = part + parts[i]  # i counts on 1: not the whole list

    return parts


def get_position(block: str) -> np.array:
    """Get the position statement at the beginning of a block for a bar"""
    regexp_pos = r"[mM]\s([0-9\.\,\-]+)"
    pos_str = re.findall(regexp_pos, block)[0]
    coords = np.array([float(value) for value in pos_str.split(",")])
    return coords


def path_to_values_bargraph(
    path: str,
) -> list:
    """Extract values from a svg bargraph path

    Will likely not work in a general case without modification
    """
    regex_block = r"[mM]\s[0-9hHzZvV\,\-\.\s]+"
    blocks = re.findall(regex_block, path)

    blocks[0] = blocks[0].replace("m", "M")  # treat first position as absolute
    values = []
    position = get_position(blocks[0])

    for block in blocks:
        # print(block)
        if block[0] == "M":
            # print("Absolute position")
            position = get_position(block)
        elif block[0] == "m":
            # print("Relative position")
            position += get_position(block)
        regexp_vertical = r"([vV]\s[0-9\.\-]+)"
        vertical = re.findall(regexp_vertical, block)[0]
        if vertical[0] == "V":
            # absolute vertical position
            values.append(-(float(vertical[2:]) - position[1]))
        else:
            # relative position
            values.append(-float(vertical[2:]))
    return values


